#El bucle for en python nos permire iterar sobre
#una variable compleja del tipo lista o tupla
miLista = ['Edgar', 'Pedro','Juanillo']
for nombre in miLista: #Nombre van a ser los elementos dentro de mi lista
    print (nombre)
segundaLista = ['morado','rojo','negro','blanco']
for colores in segundaLista:
    print (colores)
